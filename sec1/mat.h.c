#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{
    /*
    ceil floor sqrt pow abs
    
    ceil = tavana yuvarlama
	floor tabana yuvarlama
	sqrt karekök alma
	pow üslü alma
	abs mutlak deger 
    */
    printf("%f\n",ceil(4.3));   
    printf("%f\n",floor(4.9));
    printf("%f\n",sqrt(67));
    printf("%f\n",pow(3,3));
    printf("%d\n",abs(15));

    return 0;
}
